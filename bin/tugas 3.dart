void main() {
  int angka = 10;
// Pernyataan if sederhana
  if (angka > 5) {
    print('Angka lebih besar dari 5');
  }
// Pernyataan if-else
  if (angka % 2 == 0) {
    print('Angka adalah angka genap');
  } else {
    print('Angka adalah angka ganjil');
  }
// Pernyataan if-else if-else
  if (angka > 10) {
    print('Angka lebih besar dari 10');
  } else if (angka < 10) {
    print('Angka kurang dari 10');
  } else {
    print('Angka adalah 10');
  }
}
